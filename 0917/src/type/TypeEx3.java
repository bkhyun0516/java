package type;

public class TypeEx3 {
	public static void main(String[] args) {
		/*
		 * 자바의 기본자료형 실수형
		 * - float(4바이트)	: f를 붙임
		 * 
		 * 
		 * - double(8바이트):
		 * 	 자바의 기본 실수
		 */
		
		//기본자료형 float 자료형 변수 f를 선언하고 
		// 0.0으로 초기화
		//float f = 0.0; 자료형이 좌항 float, 우항이 double형으로 다르기 때문에 오류 
		float f = 0.0f;
		
		System.out.println(f);
		
		
		//기본자료형 double 자료형 변수 d를 선언하고 
		// 3.14로 초기화
		double d = 3.14;
		
		
		System.out.println(d);
		
	}
}
